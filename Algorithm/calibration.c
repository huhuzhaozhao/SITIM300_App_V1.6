/*------------------------------------------------
Function name : meanFusion
Description	  : 8组IMU先融合。没用指针传参，后续稳定后修改
Input para	  : char D：几组
				char only_T: 1仅温度 0仅陀螺加计
Return val	  : void
Update time	  : 2025-04-07-YY
------------------------------------------------*/
void meanFusion(char D, char only_T)
{
	MYTYPE num;

	if (only_T)
	{
		// 温度
		num = 0.0;
		sensor_data.T = 0.0;

		for (char j = 0; j < D; j++)
		{
			if (calib_para.Array_xyzT_8[D3][j]) // 仅计算所选IMU的温度平均值
			{
				num++;
				sensor_data.T += imu_array.IMU[j].T;
			}
		}
		sensor_data.T /= num; // 平均
	}
	else
	{
		// 陀螺 加计
		for (char i = 0; i < D3; i++)
		{
			num = 0.0; // 每轴不一样
			sensor_data.gyro[i] = 0.0;
			sensor_data.acc[i] = 0.0;

			for (char j = 0; j < D; j++)
			{
				if (calib_para.Array_xyzT_8[i][j]) // 仅计算所选IMU的平均值
				{
					num++;
					sensor_data.gyro[i] += imu_array.IMU[j].gyro[i];
					sensor_data.acc[i] += imu_array.IMU[j].acc[i];
				}
			}

			sensor_data.gyro[i] /= num; // 平均
			sensor_data.acc[i] /= num; // 平均
		}
	}
}

/*---------------(低通滤波)----------------------*/
/*------------------------------------------------
Function name : lowPassFilter
Description	  : 对单个传感器低通滤波,离散系统传递函数：
					Y(z)		  b0 + b1*z^-1 + b2*z^-2
					---- = G(z) = ----------------------
					X(z)		  a0 + a1*z^-1 + a2*z^-2
				再从离散传递函数到差分方程即可, 可等效 X(z) * z^-n = x(k-n)
Input para	  : 1. LP_FILTER* p_filter 当前传感器的储存窗口
				2. input 当前传感器新值加入
Return val	  : 单个传感器滤波结果
Update time	  : 2025-04-10-YY
------------------------------------------------*/
MYTYPE lowPassFilter(LP_FILTER* p_filter, MYTYPE input)
{
	static MYTYPE lp_b[LOW_PASS_N + 1] = { 0.04081022629,0.12243067888,0.12243067888,0.04081022629 };
	static MYTYPE lp_a[LOW_PASS_N + 1] = { 1.0,-1.29776908570066,0.7874674039,-0.1632165078858 };

	// 数组元素逐个向后移动
	for (char i = LOW_PASS_N; i >= 1; i--)
	{
		p_filter->X[i] = p_filter->X[i - 1];
		p_filter->Y[i] = p_filter->Y[i - 1];
	}

	p_filter->X[0] = input; // 更新

	MYTYPE temp = 0.0;
	// 高阶写循环  X(z) * z^-n = x(k-n)
	for (char i = 0; i <= LOW_PASS_N; i++)
	{
		temp += lp_b[i] * p_filter->X[i];
	}
	for (char i = 1; i <= LOW_PASS_N; i++)
	{
		temp -= lp_a[i] * p_filter->Y[i];
	}
	p_filter->Y[0] = temp / lp_a[0]; // 更新

	return p_filter->Y[0]; // 输出结果
}

