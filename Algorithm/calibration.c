/*-----(判定温度或转速属于哪个分段区间)--------------*/
/*------------------------------------------------
Function name : whichPart
Description	  : n=4，假设分段点为：[a b c d]，4个点分5段，区间则为：[0 1 2 3 4]
Input para	  : 1. data 当前值
				2. MYTYPE* cut 升序排列的分段点数组
				3. n 数组成员个数
Return val	  : 返回 0~n-1 之间的一个数char
Update time	  : 2025-03-28-YY
------------------------------------------------*/
char whichPart(MYTYPE data, MYTYPE* cut, char n)
{
	if (cut == NULL || n <= 0) 
	{
		return 0; // 检查输入合法性 默认返回第一个区间
	}

	if (data < cut[0]) 
	{
		return 0; // 左边界（data < 第一个分段点）
	}
	for (char i = 1; i < n; i++) // 遍历中间分段点
	{
		if (data < cut[i]) 
		{
			return i;
		}
	}
	return n;	  // 右边界（data >= 最后一个分段点）
}

