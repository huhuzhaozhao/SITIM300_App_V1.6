/*------------(矩阵相乘)-------------------------*/
/*------------------------------------------------
Function name : matrixMultiply
Description	  : MYTYPE矩阵相乘 A*B=C
Input para	  : A 的维度 m×n
				B 的维度 n×p
				C 的维度 m×p
Return val	  : 不返回 直接修改传感器原始数组指针*C
Update time	  : 2025-01-11-YY
------------------------------------------------*/
void matrixMultiply(MYTYPE* A, MYTYPE* B, MYTYPE* C, int m, int n, int p)
{
	int i, j, k;
	for (i = 0; i < m; i++)
	{
		for (j = 0; j < p; j++)
		{
			*(C + i * p + j) = 0.0;	// 数组清零
			for (k = 0; k < n; k++)
			{
				*(C + i * p + j) += *(A + i * n + k) * (*(B + k * p + j));
			}
		}
	}
}

