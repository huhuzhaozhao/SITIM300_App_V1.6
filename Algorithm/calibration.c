/*------------(变换轴向和极性)--------------------*/
/*------------------------------------------------
Function name : changeAxialAndPolarity
Description	  : 变换陀螺和加计极性 按右前上(xyz)定义
Input para	  : IMU_ARRAY* p_IMUarray 传感器原始数据结构体
Return val	  : 全局变量不返回 直接修改传感器原始数据结构体
Update time	  : 2025-04-04-YY
------------------------------------------------*/
void changeAxialAndPolarity(IMU_ARRAY* p_IMUarray, CALIB_PARA* p_para)
{
	MYTYPE t_acc[D3] = { 0.0F };
	MYTYPE t_gyro[D3] = { 0.0F };

	for (char k = 0; k < IMU_COUNT; k++)
	{
		for (char i = 0; i < D3; i++)
		{
			t_acc[i] = p_IMUarray->IMU[k].acc[i];	// 加计变换前
			t_gyro[i] = p_IMUarray->IMU[k].gyro[i];	// 陀螺变换前
		}
		for (char i = 0; i < D3; i++)		// 变换后
		{
			p_IMUarray->IMU[k].acc[i] = p_para->A_pola[i] * t_acc[(char)p_para->A_axis[i]];
			p_IMUarray->IMU[k].gyro[i] = p_para->G_pola[i] * t_gyro[(char)p_para->G_axis[i]];
		}
	}
}

